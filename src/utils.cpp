#include <cstddef>
#include <string>

namespace lex {

////////////////////////////////////////////////////////////////////////////////
//! count digits
////////////////////////////////////////////////////////////////////////////////
size_t count_digits(int i)
{
  int count = 0;
  while (i != 0) {
    i /= 10;
    count++;
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
//! string comparison
////////////////////////////////////////////////////////////////////////////////
bool differs(const std::string & a, const std::string & b)
{
  auto na = a.size();
  if (na != b.size()) return true;

  for (size_t i=0; i<na; ++i)
    if (a[i] != b[i]) return true;
  
  return false;
}

} // namespace
